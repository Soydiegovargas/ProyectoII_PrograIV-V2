-------------TABLA CCSS-------------------

--STORE PROCEDURES LISTAR - INSERTAR - MODIFICAR - FILTRAR - ELIMINAR

CREATE PROCEDURE [SCH_ADMIN].[sp_Listar_CCSS]
AS
BEGIN
SELECT [CED_JUR]			
      ,[TELEFONO]		
	  ,[PAGINA]				
	  ,[DESCRIPCION]		
  FROM [SCH_ADMIN].[T_CCSS]
  END
GO

CREATE PROCEDURE [SCH_ADMIN].[sp_Insertar_CCSS]
(
			@CED_JUR  nvarchar(10),
            @TELEFONO int,
            @PAGINA nchar(15),
			@DESCRIPCION nchar(40)
)
AS
BEGIN
INSERT INTO [SCH_ADMIN].[T_CCSS]
           ([CED_JUR]
           ,[TELEFONO]
           ,[PAGINA]
		   ,[DESCRIPCION])
     VALUES
           (
		   @CED_JUR,
		   @TELEFONO,
		   @PAGINA,
		   @DESCRIPCION
		   )
END
GO

CREATE PROCEDURE [SCH_ADMIN].[sp_Modificar_CCSS]
(
			@CED_JUR		nvarchar(10),
            @TELEFONO		int,
            @PAGINA			nchar(15),
			@DESCRIPCION	nchar(40)
)
AS
BEGIN
UPDATE [SCH_ADMIN].[T_CCSS]
   SET 
       [TELEFONO] = @TELEFONO,
       [PAGINA] = @PAGINA,
	   [DESCRIPCION] = @DESCRIPCION
 WHERE [CED_JUR] = @CED_JUR
 END
GO

CREATE PROCEDURE [SCH_ADMIN].[sp_Filtrar_CCSS]
(
	@Filtro  NCHAR(50) --"@Filtro" es la primera columna del DataTable, NCHAR(50) es la segunda columna, osea el tipo de dato, la tercera columna es el valor que ya trae dentro
)			
AS
BEGIN

SELECT [CED_JUR]
      ,[PAGINA]
      ,[TELEFONO]
	  ,[DESCRIPCION]
  FROM [SCH_ADMIN].[T_CCSS]
  WHERE LTRIM (RTRIM ([CED_JUR])) LIKE '%' + LTRIM (RTRIM (@Filtro)) + '%' 
END
GO

CREATE PROCEDURE [SCH_ADMIN].[sp_Eliminar_CCSS]
(
			@CED_JUR nvarchar(10)
            
)
AS
BEGIN
DELETE FROM [SCH_ADMIN].[T_CCSS]
      WHERE [CED_JUR] =  @CED_JUR
END
GO

